<?php namespace App;

use Illuminate\Database\Eloquent\Model;

class Visit extends Model {

  // --------------------------------------------------------------------------
  // Relaciones
  // --------------------------------------------------------------------------

  // --------------------------------------------------------------------------
  // Belongs to
  // --------------------------------------------------------------------------
  public function user()
  {
    return $this->belongsTo('App\User');
  }

  // --------------------------------------------------------------------------
  // Polymorphic
  // --------------------------------------------------------------------------
  public function visitable()
  {
    return $this->morphTo();
  }

}
